#!/usr/bin/env node
import {
  copyFileSync,
  existsSync,
  mkdirSync,
  readFileSync,
  statSync,
  writeFileSync,
} from "node:fs";
import { dirname, isAbsolute, join, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const srcTauri = join(root, "src-tauri");
const localResourceDir = join(srcTauri, "windows", "wix", "resources");
const tauriWixResourceDir = join(srcTauri, "target", "release", "bundle", "wix", "resources");

const args = new Set(process.argv.slice(2));
const isCi = process.env["CI"] === "true";
const noPlaceholders = args.has("--no-placeholders") || args.has("--strict");
const allowPlaceholders = args.has("--allow-placeholders") || (!isCi && !noPlaceholders);
const strict = args.has("--strict") || isCi;

const assets = [
  {
    label: "NSSM service wrapper",
    fileName: "nssm.exe",
    envVar: "NSSM_EXE",
    candidates: [
      process.env["NSSM_EXE"],
      join(localResourceDir, "nssm.exe"),
      join(srcTauri, "windows", "bin", "nssm.exe"),
      join(tauriWixResourceDir, "nssm.exe"),
    ],
    guidance: [
      "Download NSSM 2.24 from https://nssm.cc/release/nssm-2.24.zip and copy win64/nssm.exe to src-tauri/windows/bin/nssm.exe.",
      "Or set NSSM_EXE=/absolute/path/to/nssm.exe before running this script.",
    ],
  },
  {
    label: "ARUS backend sidecar",
    fileName: "arus-server.exe",
    envVar: "ARUS_SERVER_EXE",
    candidates: [
      process.env["ARUS_SERVER_EXE"],
      join(localResourceDir, "arus-server.exe"),
      join(srcTauri, "binaries", "arus-server-x86_64-pc-windows-msvc.exe"),
      join(srcTauri, "binaries", "arus-server.exe"),
      join(tauriWixResourceDir, "arus-server.exe"),
    ],
    guidance: [
      "Run npm run build:sidecar on Windows to produce src-tauri/binaries/arus-server-x86_64-pc-windows-msvc.exe.",
      "Or set ARUS_SERVER_EXE=/absolute/path/to/arus-server.exe before running this script.",
    ],
  },
];

function rel(path) {
  return relative(root, path).replace(/\\/g, "/");
}

function resolveMaybe(path) {
  if (!path) return null;
  return isAbsolute(path) ? path : resolve(root, path);
}

function ensureDir(path) {
  mkdirSync(path, { recursive: true });
}

function isPresentFile(path) {
  if (!path || !existsSync(path)) return false;
  const stat = statSync(path);
  return stat.isFile() && stat.size > 0;
}

function isPlaceholderFile(path) {
  if (!isPresentFile(path)) return false;
  try {
    return readFileSync(path, "utf8").startsWith("ARUS LOCAL PLACEHOLDER BINARY");
  } catch {
    return false;
  }
}

function firstExistingCandidate(candidates) {
  let placeholder = null;
  for (const candidate of candidates) {
    const resolved = resolveMaybe(candidate);
    if (!isPresentFile(resolved)) continue;
    if (isPlaceholderFile(resolved)) {
      placeholder ??= resolved;
      continue;
    }
    return { path: resolved, placeholder: false };
  }
  return placeholder ? { path: placeholder, placeholder: true } : null;
}

function writeResourceGitignore() {
  const path = join(localResourceDir, ".gitignore");
  if (!existsSync(path)) {
    writeFileSync(path, "*\n!.gitignore\n", "utf8");
  }
}

function copyIfNeeded(source, destination) {
  if (resolve(source) === resolve(destination)) return;
  ensureDir(dirname(destination));
  copyFileSync(source, destination);
}

function writePlaceholder(asset, destination) {
  const text = [
    "ARUS LOCAL PLACEHOLDER BINARY",
    "",
    `Generated by scripts/stage-local-wix.js for ${asset.fileName}.`,
    "This is only for local Tauri/WiX config or schema validation on machines that cannot build Windows binaries.",
    "Do not use this file for a real installer, release, smoke test, or service install.",
    "",
    "To replace it with the real asset:",
    ...asset.guidance.map((line) => `- ${line}`),
    "",
  ].join("\n");

  ensureDir(dirname(destination));
  writeFileSync(destination, text, "utf8");
}

function warnMissing(asset) {
  console.warn(`\n[stage-local-wix] Missing ${asset.label} (${asset.fileName}).`);
  console.warn("[stage-local-wix] Real asset options:");
  for (const line of asset.guidance) {
    console.warn(`  - ${line}`);
  }
  if (allowPlaceholders) {
    console.warn(
      "[stage-local-wix] Creating local-only placeholders so non-Windows Tauri config checks can continue."
    );
  } else {
    console.warn(
      "[stage-local-wix] Placeholder creation is disabled. Re-run with --allow-placeholders for schema-only local checks."
    );
  }
}

function stageAsset(asset) {
  const localDestination = join(localResourceDir, asset.fileName);
  const tauriDestination = join(tauriWixResourceDir, asset.fileName);
  const source = firstExistingCandidate(asset.candidates);

  if (source && !source.placeholder) {
    copyIfNeeded(source.path, localDestination);
    copyIfNeeded(localDestination, tauriDestination);
    console.log(`[stage-local-wix] staged ${asset.fileName} from ${rel(source.path)}`);
    return { fileName: asset.fileName, status: "staged", source: source.path };
  }

  if (source?.placeholder && allowPlaceholders) {
    copyIfNeeded(source.path, localDestination);
    copyIfNeeded(localDestination, tauriDestination);
    console.warn(
      `[stage-local-wix] using existing local-only placeholder for ${asset.fileName}; replace it before a real installer build.`
    );
    return { fileName: asset.fileName, status: "placeholder" };
  }

  warnMissing(asset);
  if (allowPlaceholders) {
    writePlaceholder(asset, localDestination);
    copyIfNeeded(localDestination, tauriDestination);
    console.log(`[stage-local-wix] placeholder ${asset.fileName} -> ${rel(localDestination)}`);
    return { fileName: asset.fileName, status: "placeholder" };
  }

  return { fileName: asset.fileName, status: "missing" };
}

function main() {
  ensureDir(localResourceDir);
  ensureDir(tauriWixResourceDir);
  writeResourceGitignore();

  console.log(`[stage-local-wix] local cache: ${rel(localResourceDir)}`);
  console.log(`[stage-local-wix] Tauri WiX resources: ${rel(tauriWixResourceDir)}`);

  const results = assets.map(stageAsset);
  const missing = results.filter((result) => result.status === "missing");
  const placeholders = results.filter((result) => result.status === "placeholder");

  console.log("\n[stage-local-wix] Summary:");
  for (const result of results) {
    console.log(`  - ${result.fileName}: ${result.status}`);
  }

  if (placeholders.length > 0) {
    console.warn(
      "\n[stage-local-wix] Placeholder binaries are local-only and intentionally ignored by git."
    );
    console.warn(
      "[stage-local-wix] Replace them with real binaries before running a Windows installer build."
    );
  }

  if (strict && missing.length > 0) {
    console.error("\n[stage-local-wix] Required WiX resources are missing.");
    process.exitCode = 1;
  }
}

main();
