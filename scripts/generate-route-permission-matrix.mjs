import { mkdirSync, readdirSync, readFileSync, statSync, writeFileSync } from "node:fs";
import { dirname, relative, resolve } from "node:path";

const root = process.cwd();
const serverRoot = resolve(root, "server");
const outFile = resolve(root, "docs/qa/route-permission-matrix.md");

function walk(dir, files = []) {
  for (const entry of readdirSync(dir)) {
    if (entry === "node_modules" || entry === "dist" || entry.startsWith(".")) continue;
    const full = resolve(dir, entry);
    const stat = statSync(full);
    if (stat.isDirectory()) {
      walk(full, files);
    } else if (full.endsWith(".ts")) {
      files.push(full);
    }
  }
  return files;
}

function firstQuoted(lines, startIndex) {
  for (let i = startIndex; i < Math.min(lines.length, startIndex + 10); i += 1) {
    const match = lines[i].match(/["'`]([^"'`]*\/api\/[^"'`]*)["'`]|["'`]([^"'`]+)["'`]/);
    if (match) return match[1] ?? match[2] ?? "unknown";
  }
  return "unknown";
}

function escapeCell(value) {
  return String(value).replaceAll("|", "\\|").replace(/\s+/g, " ").trim();
}

const rows = [];

for (const file of walk(serverRoot)) {
  const rel = relative(root, file);
  const lines = readFileSync(file, "utf8").split(/\r?\n/);
  let route = { method: "?", path: "unknown", line: 0 };

  lines.forEach((line, index) => {
    const trimmed = line.trim();
    if (trimmed.startsWith("//") || trimmed.startsWith("*")) {
      return;
    }

    const routeMatch = line.match(/\b(?:app|router)\.(get|post|put|patch|delete)\s*\(/);
    if (routeMatch) {
      route = {
        method: routeMatch[1].toUpperCase(),
        path: firstQuoted(lines, index),
        line: index + 1,
      };
    }

    const permissionMatch = line.match(/requirePermission\(\s*["'`]([^"'`]+)["'`]\s*,\s*["'`]([^"'`]+)["'`]\s*\)/);
    if (permissionMatch) {
      rows.push({
        file: rel,
        line: index + 1,
        method: route.method,
        path: route.path,
        guard: "requirePermission",
        requirement: `${permissionMatch[1]}:${permissionMatch[2]}`,
      });
    }

    const roleMatch = line.match(/requireRole\(([^)]*["'`][^)]*)\)/);
    if (roleMatch) {
      const roles = [...roleMatch[1].matchAll(/["'`]([^"'`]+)["'`]/g)].map((m) => m[1]);
      rows.push({
        file: rel,
        line: index + 1,
        method: route.method,
        path: route.path,
        guard: "requireRole",
        requirement: roles.join(", "),
      });
    }
  });
}

rows.sort((a, b) =>
  a.path.localeCompare(b.path) ||
  a.method.localeCompare(b.method) ||
  a.file.localeCompare(b.file) ||
  a.line - b.line
);

const now = new Date().toISOString();
const markdown = [
  "# Route Permission Matrix",
  "",
  `Generated by \`scripts/generate-route-permission-matrix.mjs\` at ${now}.`,
  "",
  `Total guarded route call sites: ${rows.length}.`,
  "",
  "| Method | Route | Guard | Requirement | Source |",
  "| --- | --- | --- | --- | --- |",
  ...rows.map(
    (row) =>
      `| ${escapeCell(row.method)} | ${escapeCell(row.path)} | ${escapeCell(row.guard)} | ${escapeCell(row.requirement)} | ${escapeCell(`${row.file}:${row.line}`)} |`
  ),
  "",
].join("\n");

mkdirSync(dirname(outFile), { recursive: true });
writeFileSync(outFile, markdown);
console.log(`Wrote ${relative(root, outFile)} with ${rows.length} guarded route call sites.`);
